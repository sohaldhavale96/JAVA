public class Java3 {
    public static void main(String[] args) {
        // Arithmetic operators
        
        int num1 = 12;
        int num2 = 45;
        int add = num1+num2;
        int sub = num2-num1;
        int div = num2/num1;
        int mult = num1*num2;
        System.out.println(add);
        System.out.println(sub);
        System.out.println(mult);
        System.out.println(div);
        num1++;
        System.out.println(num1);
        num1--;
        System.out.println(num1);
    }    
}