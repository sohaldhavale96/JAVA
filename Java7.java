public class Java7 {
    public static void main(String[] args) {
        // ternary operator
        int num = 19;
        boolean isEven;
        isEven = num%2==0 ? true : false;
        System.out.println(isEven);
        num = 10;
        isEven = num%2==0 ? true : false;
        System.out.println(isEven);
    }
}
